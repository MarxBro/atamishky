<h1>Acerca de la forma de uso del buscador</h1>

<p>Para comenzar una b&uacute;squeda, utilice la solapa de la izquierda y seleccione el criterio de filtrado.</p>

<h1>Criterios de b&uacute;squeda predefinidos</h1>

<p>Puede seleccionar alguno de los siguientes criterios:</p>

<ul>
<li>TODO</li>
<li>Titulo</li>
<li>Autor</li>
<li>Descripcion</li>
<li>Libros</li>
<li>Musica</li>
<li>Videos</li>
<li>Otros</li>
</ul>

<p>Los primeros cuatro criterios afectan a cualquier tipo de material: es
indistinto si es un libro o una pelicula, por ejemplo.</p>

<p>Si la opción <strong>T&iacute;tulo</strong> es utilizada como criterio, se buscan
todos los t&iacute;tulos que coincidan con el texto introducido.</p>

<p>Similarmente, si se utiliza el texto buscado como parte de la <strong>descripción</strong> de
algun material bibliografico del catalogo, bajo una busqueda del mismo
criterio, se conforma una lista de las entradas resultantes.</p>

<p><i>Por ejemplo, si un grupo de peliculas incluyen la palabra "Audiovision" en su
descipcion, buscando por Descripcion el texto "audiov" el grupo completo
aparece en los resultados (posiblemente acompañado de resultados adicionales,
ya que el texto introducido no es exactamente "audiovision", pero coincide).
</i></p>

<p>La opcion <strong>Autor</strong> filtra en la lista total de autores, por
los nombres y/o apellidos que lo incluyan. Asi, "ado" incluye a Adorno y a
Adolfo Bioy Casares, por ejemplo.</p>

<p>Los ulttimos cuatro criterios, en cambio, <i>buscan cualquier texto en
los  materiales que concuerden con el tipo</i>: por ejemplo, una busqueda de
"music" en el criterio por defecto (TODOS) va a mostrar como resultados validos
materiales que pudiesen ser libros o musica. En cambio, si se utiliza el
criterio "Libros", todas las entradas que no sean de este tipo son filtradas de
antemano: busca únicamente en el caso de las citas que pertenezcan al mismo
tipo.</p>

<h1>Criterios de busqueda dinamicos</h1>

<p>Adicionalmente, <strong>es posible utilizar -casi- cualquier criterio como 
filtrado dinamico</strong>: autores, titulos, idomas, soporte, editoriales...
Alcanza con cliquear en el enlace para convertir al elemento en un filtro.</p>

<p>En el encabezado de la página se especifica el tipo de filtrado actual.</p>

<h1>Criterios de organizacion del catalogo adicionales</h1>

<p>Estos son dos (por el momento) y funcionan como colecciones de referencias: una
categorizacion inmediatamente superior al tipo de material que agrupa varias
entradas y funciona como conjunto.</p>


<ul>
    <li>Tag</li>
    <li>Bibliografia</li>
</ul>

<p>Los tags o etiquetas, sirven para clasificar cualquier referencia acorde a un
concepto, que permita relacionar varias entradas.</p>

<p>La opcion <strong>ver todos</strong> muestra la lista de tags completa; los
tags con mayores ocurrencias dentro del catalogo se ven mas grandes. Para ver
los tags relacionados a alguna entrada en particular, alcanza con cosultar la
informacion detallada de la entrada, a través del enlace <strong>mas
info</strong> de la derecha.</p>


