<h1>Acerca de la forma de uso del buscador</h1>

<p>Para comenzar una b&uacute;squeda, utilice la solapa de la izquierda y seleccione el criterio de filtrado.</p>

<p><strong>Todas las búsquedas son indiferentes a mayúsculas y minúsculas</strong> (case-insensitive).</p>

<h1>Criterios de b&uacute;squeda predefinidos</h1>

<p>Puede seleccionar alguno de los siguientes criterios:</p>

<ul>
<li>TODO</li>
<li>T&iacute;tulo</li>
<li>Autor</li>
<li>Descripci&oacute;n</li>
<li>Libros</li>
<li>M&uacute;sica</li>
<li>Videos</li>
<li>Otros</li>
</ul>

<p>Los primeros cuatro criterios afectan a cualquier tipo de material: es
indistinto si es un libro o una película, por ejemplo.</p>

<p>Si la opción <strong>T&iacute;tulo</strong> es utilizada como criterio, se buscan
todos los t&iacute;tulos que coincidan con el texto introducido.</p>

<p>Similarmente, si se utiliza el texto buscado como parte de la <strong>descripción</strong> de
algun material bibliográfico del catálogo, bajo una búsqueda del mismo
criterio, se conforma una lista de las entradas resultantes.</p>

<p><i>Por ejemplo, si un grupo de peliculas incluyen la palabra "Audiovision" en su
descipcion, buscando por <b>Descripción</b> el texto "audiov" el grupo completo
aparece en los resultados (posiblemente acompañado de resultados adicionales,
ya que el texto introducido <u>no es exactamente</u> "audiovision", <u>pero coincide</u>).
</i></p>



<p>La opción <strong>Autor</strong> filtra en la lista total de autores, por
los nombres y/o apellidos que lo incluyan. Asi, "ado" incluye a <u>Adorno</u> y a
<u>Adolfo Bioy Casares</u> como resultados posibles, por ejemplo.</p>

<p>Los últtimos cuatro criterios, en cambio, <i>buscan cualquier texto en
los  materiales que concuerden con el tipo</i>: por ejemplo, una búsqueda de
"music" en el criterio por defecto (TODOS) va a mostrar como resultados validos
materiales que pueden ser del tipo libro o música. En cambio, si se utiliza el
criterio <strong>Libros</strong>, todas las entradas que no sean de este tipo son filtradas de
antemano: busca únicamente en el caso de las entradas del catálogo que pertenezcan al mismo
tipo.</p>

<h1>Criterios de búsqueda dinámicos</h1>

<p>Adicionalmente, <strong>es posible utilizar -casi- cualquier criterio como 
filtrado dinámico</strong>: autores, títulos, idiomas, soporte, editoriales...
Alcanza con cliquear en el enlace para convertir al elemento en un filtro.</p>

<p>En el encabezado de la página se especifica el tipo de filtrado actual.</p>

<h1>Criterios de organización del catálogo adicionales</h1>

<p>Estos son dos (por el momento) y funcionan como <u>colecciones de referencias</u>: una
categorización inmediatamente superior al tipo de material que agrupa varias
entradas y funciona como conjunto.</p>


<ul>
    <li>Tag</li>
    <li>Bibliografia</li>
</ul>

<p>Los <strong>tags</strong> o etiquetas, sirven para clasificar cualquier referencia acorde a un
concepto, que permita relacionar varias entradas.</p>

<p>La opción <strong>ver todos</strong> muestra la lista de tags completa; los
tags con mayores ocurrencias dentro del catálogo se ven mas grandes. Para ver
los tags relacionados a alguna entrada en particular, alcanza con consultar la
informacion detallada de la entrada, a través del enlace <strong>mas
info</strong> de la derecha.</p>

