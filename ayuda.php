<div class="title">Ayuda</div>
<div class="entrybody">

<p>Para comenzar una busqueda, utilice la solapa de la izquierda y seleccione el criterio de filtrado.</p>

<p>Puede seleccionar alguno de los siguientes criterios:</p>

<ul>
<li>TODO</li>
<li>Titulo</li>
<li>Autor</li>
<li>Descripcion</li>
<li>Libros</li>
<li>Musica</li>
<li>Videos</li>
<li>Otros</li>
</ul>

<p>Los primeros cuatro criterios afectan a cualquier tipo de material: es indistinto si es un libro o una pelicula.</p>

<p>Si la opción <strong>Titulo</strong> es utilizada como criterio, se buscaran todos los titulos que coincidan con el texto introducido. Similarmente, si se utiliza el texto buscado como parte de la descripcion de algun material bibliografico, bajao una busqueda del mismo criterio, el resultado aparecera en la lista.</p>

<p>La opcion <strong>Autor</strong> filtra en la lista total de autores, por los nombres y/o apellidos que lo incluyan. Asi, "ado" incluye a Adorno y a Adolfo Bioy Casare, por ejemplo.</p>

<p>Los ulttimos cuatro criterios, en cambio, buscan cualquier tipo de texto en los  materiales que concuerden con el tipo: por ejemplo, una busqueda de "music" en el criterio por defecto (TODOS) va a mostrar como resultados validos materiales que pudiesen ser libros o musica. En cambio, si se utiliza el criterio "Libros", todas las entradas que no sean de este tipo son filtradas de antemano: busca únicamente en el caso de las citas que pertenezcan al mismo tipo.</p>

<p>Adicionalmente, es posible utilizar -casi- cualquier criterio desde los titulos como filtrado: autores, titulos, idomas, soporte, editoriales... Alcanza con cliquear en el enlace para convertir al elemento en un filtro.</p>


